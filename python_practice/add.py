a = int(input("숫자를 입력하세요(a) : "))
b = int(input("숫자를 입력하세요(b) : "))


def add(a, b):
    result = a + b
    print(a, ' + ', b, ' = ', result, '\n')


def sub(a, b):
    result = a - b
    print(a, ' - ', b, ' = ', result, '\n')


def mul(a, b):
    result = a * b
    print(a, ' * ', b, ' = ', result, '\n')


def div(a, b):
    result = a / b
    print(a, ' / ', b, ' = ', result, '\n')


add(a,b)
sub(a,b)
mul(a,b)
